m_catch <-
  mrip_catch %>% 
  select(-tot_len,-wgt_ab1,-CLAIM,
         -RELEASE,-HARVEST,
         -tot_len_b1,-wgt_b1,-tot_len_a,-wgt_a,
         #-HARVEST_UNADJ,
